class FindPrime {

	public static void main(String args[]) {

	int pc;
	int bound = 100;

		for (pc = 2; pc <= bound; pc++) {
			int dc;
			boolean is_prime = true;
			for (dc = 2; dc <= (int) Math.sqrt(pc); dc++) {
				if ((pc % dc) == 0) {
					is_prime = false;
				}
      }
				if (is_prime) {
					System.out.println("Number " + pc + " is prime.");
				}
		}
	}
}
